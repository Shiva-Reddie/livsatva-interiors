<script>
const stepsData = {
    1: {
        title: 'Space Measurement & Estimation',
        subtitle: 'We start with precise measurements of your office space and provide a transparent cost estimation.',
        note: 'Accurate workspace planning with clear quotation'
    },
    2: {
        title: 'Design & Customization',
        subtitle: 'Our designers create tailored 2D/3D office furniture layouts including desks, cabinets, and partitions.',
        note: 'Personalized 2D/3D office furniture designs'
    },
    3: {
        title: 'Customer Approval',
        subtitle: 'We share the final office layout for your review, and production starts only after your approval.',
        note: 'Production begins after your approval'
    },
    4: {
        title: 'Precision Manufacturing',
        subtitle: 'Office furniture components are crafted using advanced machinery and premium materials for durability and aesthetics.',
        note: 'Crafted with high-quality materials'
    },
    5: {
        title: 'Delivery & Installation',
        subtitle: 'Your modular office furniture is delivered safely and installed on time for a fully functional workspace.',
        note: 'Delivered & installed on schedule'
    }
};




const steps = Array.from(document.querySelectorAll('.step'));
const progress = document.getElementById('progressBar');
const detailTitle = document.getElementById('detailTitle');
const detailSubtitle = document.getElementById('detailSubtitle');
const currentStepText = document.getElementById('currentStep');
const prevBtn = document.getElementById('prevBtn');
const nextBtn = document.getElementById('nextBtn');

let current = 1;

function updateUI() {
    steps.forEach((el) => {
        const s = Number(el.dataset.step);
        el.classList.toggle('active', s === current);
        el.classList.toggle('inactive', s !== current);
    });
    const pct = ((current - 1) / (steps.length - 1)) * 100;
    progress.style.width = pct + '%';
    detailTitle.textContent = stepsData[current].title;
    detailSubtitle.textContent = stepsData[current].subtitle;
    currentStepText.textContent = current;
    prevBtn.disabled = current === 1;
    nextBtn.disabled = current === steps.length;
}

steps.forEach(s => s.addEventListener('click', () => {
    current = Number(s.dataset.step);
    updateUI();
}));

prevBtn.addEventListener('click', () => {
    if (current > 1) {
        current--;
        updateUI();
    }
});
nextBtn.addEventListener('click', () => {
    if (current < steps.length) {
        current++;
        updateUI();
    }
});

document.addEventListener('keydown', (e) => {
    if (e.key === 'ArrowRight') {
        if (current < steps.length) {
            current++;
            updateUI();
        }
    }
    if (e.key === 'ArrowLeft') {
        if (current > 1) {
            current--;
            updateUI();
        }
    }
});

updateUI();
</script>


